declare(strict_types=1);

namespace ActiveCollab\Bootstrap\App;

class Path implements PathInterface
{
    private $path;

    public function __construct(string $path)
    {
        $this->path = $path;
    }

    public function getPath(): string
    {
        return $this->path;
    }
}
